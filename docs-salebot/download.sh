SUCCESS=0
FAIL=0

for URL in "${URLS[@]}"; do
    # Получаем путь относительно BASE
    REL="${URL#$BASE/}"
    # Убираем .md из имени, создаём папку
    DIR="$OUT/$(dirname "$REL")"
    mkdir -p "$DIR"
    OUTFILE="$OUT/$REL"

    HTTP_CODE=$(curl -s -o "$OUTFILE" -w "%{http_code}" -L \
        -H "User-Agent: Mozilla/5.0" \
        --max-time 15 \
        "$URL")

    if [ "$HTTP_CODE" = "200" ]; then
        SIZE=$(wc -c < "$OUTFILE")
        if [ "$SIZE" -lt 100 ]; then
            echo "EMPTY ($SIZE bytes): $REL"
            ((FAIL++))
        else
            ((SUCCESS++))
        fi
    else
        echo "FAIL ($HTTP_CODE): $REL"
        rm -f "$OUTFILE"
        ((FAIL++))
    fi
done

